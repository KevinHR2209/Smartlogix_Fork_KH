CREATE TABLE bodega (
    id_bodega INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(100),
    direccion_fisica VARCHAR(200)
);

CREATE TABLE producto (
    id_producto BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    sku VARCHAR(50) UNIQUE,
    nombre VARCHAR(150),
    descripcion TEXT,
    precio_actual INT,
    peso_gramos INT,
    dimensiones VARCHAR(50),
    estado VARCHAR(50)
);

CREATE TABLE producto_bodega (
    id_inventario BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_bodega INT NOT NULL,
    id_producto BIGINT NOT NULL,
    stock_disponible INT,
    stock_reservado INT,
    CONSTRAINT fk_producto_bodega_bodega
        FOREIGN KEY (id_bodega) REFERENCES bodega(id_bodega),
    CONSTRAINT fk_producto_bodega_producto
        FOREIGN KEY (id_producto) REFERENCES producto(id_producto)
);

